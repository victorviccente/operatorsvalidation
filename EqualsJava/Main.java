class Main {
  public static void main(String[] args) {
    int numero1 = 12;
    int numero2 = 15;

    System.out.println(numero1 == numero2); // false

    System.out.println(numero1 > numero2); // false
    
    System.out.println(numero1 < numero2); // true

    System.out.println(numero1 >= numero2); // false

    System.out.println(numero1 <= numero2); // true

    System.out.println(numero1 != numero2); // true
    
  }
}